
public class DataTypes {

	public static void main(String[] args) {
		
		//byte
		byte b1=25;
		byte b2=-128;
		byte b3=127;
		System.out.println(b1+"|"+b1+"|"+b3);
		//byte b4=128;
		
		System.out.println("-------------------");
		
		//short
		short s1=500;
		short s2=-32768;
		short s3=32767;
		System.out.println(s1+"|"+s2+"|"+s3);
		//short s4=32768;
		
		System.out.println("-------------------");
		
		//int
		int i1=-50000;
		int i2=50000;
		int i3=100_000;
		System.out.println(i1+"|"+i2+"|"+i3);
		
		System.out.println("-------------------");
		
		//long
		long l1=-50000;
		long l2=100_000_000;
		long l3=214783949403l;
		System.out.println(l1+"|"+l2+"|"+l3);
		
		System.out.println("-------------------");
		
		//float; floating point types have double as default type
		float f1=5.27f;
		double d1=5.27;
		
		System.out.println("-------------------");
		
		//char
		char c1='D';
		char c2='2';
		char c3='@';
		System.out.println(c1+"|"+c2+"|"+c3);

		
		//unicode-table.com
		char myChar='\u00A9';
		char registeredSymbol='\u00AE';
		System.out.println(myChar + "|" + registeredSymbol);
		
		System.out.println("-------------------");
		
		//boolean
		boolean bool1=true;
		boolean bool2=false;
		System.out.println(bool1+"|"+bool2);


		
		

		

		

		
		
		
		
		

	}

}
